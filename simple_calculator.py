#Simple-Calculator
#A simple calculator used to make operations like Addition,Subtraction,Multiplication and Division by just passing 2 numbers.


a=int(input('Enter the first number:'))            #Passing first number
b=int(input('Enter the second number:'))           #Passing second number
print('The sum is:',a+b)                           #Adds the 2 numbers
print('The difference is:',a-b)                    #Subtracts the 2 numbers
print('The product is:',a*b)                       #Multiplies the 2 numbers
print('The quotient is:',a/b)                      #Divides the 2 numbers